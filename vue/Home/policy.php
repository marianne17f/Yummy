                 <h3>Utilisation des cookies que vous pouvez désactiver</h3>
                     <p>Un cookie est un petit fichier texte qui est
transféré sur votre ordinateur, smartphone ou tablette par le biais de
votre navigateur Internet. Les cookies sont enregistrés sur le disque
dur de votre terminal (ordinateur, smartphone ou tablette) lorsque vous
visitez notre site. Les cookies permettent de vous reconnaître lors de
vos visites suivantes sur notre site.<br>
                     Les cookies peuvent être temporaires (cookies de
session) ou permanents. Les cookies de session sont actifs le temps de
votre visite uniquement et supprimés lorsque vous fermez le navigateur.
Les cookies permanents restent sur le disque dur de votre terminal
lorsque vous quittez le site et y demeurent jusqu’à ce que vous les
supprimiez manuellement ou que le navigateur les purge après un certain
délai.<br>
                     Nous utilisons des cookies de performance
analytique pour surveiller la performance du site Internet, par exemple
pour déterminer le nombre de fois qu’une page est consultée et le nombre
d’utilisateurs uniques sur son site Internet. Les services d’analyse Web
peuvent être conçus et exploités par des tiers. Les informations
fournies par ces cookies s’utilisent pour optimiser l’expérience de
l’utilisateur ou pour identifier les parties du site Internet qui
peuvent nécessiter des interventions de maintenance. Les informations
sont anonymes et sont uniquement utilisées pour des statistiques
d’audience.</p>

                     <p>Nous pouvons utiliser notamment Google Analytics
pour recueillir des informations sur les visiteurs du site grâce à ces
cookies. Ces informations peuvent être utilisées par les fournisseurs de
ces solutions.</p>

